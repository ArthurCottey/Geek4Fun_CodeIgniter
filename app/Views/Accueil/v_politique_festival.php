    <p class="text-politique-festival text-justify">

        Le festival est un lieu de partage visité par un grand nombre de personnes où chacun
        doit pouvoir passer un bon moment.<br>
        C'est un espace labellisé « Respect Zone », une ONG qui offre une charte du respect
        et des outils positifs de communication non violente en ligne et hors ligne. En nous
        labellisant Respect Zone, notre festival s’inscrit dans le travail de
        <a href="www.respectzone.org">www.respectzone.org</a> pour l’éducation au numérique, la liberté́ de l’expression
        responsable, la prévention de la cyber violence et la protection de l’enfance.<br>
        La Charte du respect sur Internet de Respect Zone est disponible sur :<br>
        <a href="www.respectzone.org">www.respectzone.org</a><br>
        Pour permettre à tous de pouvoir passer une journée rythmée par la passion des jeux
        vidéo, et sans inquiétude, voici quelques règles à respecter pour le bien-être de tous
        et afin de créer ensemble une Respect Zone.<br>
        Le bien-être de tout passe par un comportement responsable et du respect.
        En participant au festival Geek 4 Fun, je m'engage donc à respecter la Charte de
        www.respectzone.org, mais aussi à :<br>
        J'ADOPTE UN COMPORTEMENT RESPECTUEUX
        Un comportement responsable nécessite de réagir rapidement si lors de ma visite,
        j’aperçois quelqu’un en difficulté́ ou si je suis en difficulté.<br>
        Aussi, dans ce cas, n’hésitez pas à nous contacter. Au travers nos équipes sur place,
        mais aussi à ce numéro : 02.47.99.99.99 (Attention, le recours abusif à ce numéro
        sera relevé́ et sanctionné), ou bien sur les réseaux sociaux via : Messenger Festival
        Geek4Fun.<br>

        J'ADOPTE LES REGLES DU RESPECTE SUIVANTE :<br><br>
        <h2>1. le comportement</h2><br>
        La liberté́ de l’expression, de la pensée et de la critique sont des droits fondamentaux
        que nous défendons. Aussi, j’écoute l’autre et j’adopte une communication non-
        violente et responsable, en conformité́ avec la Charte d’auto modération Respect Zone.<br>
        Tous les participants du festival, qu’ils soient exposants, visiteurs, animateurs, invités
        ou staff, me doivent le respect et méritent mon respect.
        Je traite chacun avec amabilité́ et bienveillance.<br><br>
        <h2>2. le respect et l'intégrité</h2><br>
        La violence n’est pas admise au sein du festival. Chaque participant à l’évènement
        mérite d’être respecté et de pouvoir profiter du festival en toute sécurité. Aussi, si je
        fais l’objet d’un signalement ou si je suis vu en train d’agir de manière violente, je sais
        que des mesures seront prises. Pour rappel, une violence est un acte d’agression,
        une attaque physique, verbale ou menaçante envers une personne.<br>
        Risques encourus : Les coups et blessures sont passibles de 750 € d’amende ainsi
        que d’éventuelles peines d’emprisonnement.<br>
        Mesures appliquées pendant le festival : <br>Tout comportement violent entraînera
        l’exclusion de son auteur<br><br>
        <h2>3. la communication</h2><br>
        Je m’engage à ne pas tenir des propos :
        harcelants. <br>Rappel : le harcèlement consiste en des propos ou
        comportements hostiles ayant pour but de blesser la personne qui subit ce
        harcèlement.<br>
        Le harcèlement sexuel est le fait d’imposer à une personne, des propos ou
        comportements à connotation sexuelle. Ces comportements peuvent être des
        taquineries, des blagues, des insinuations, des photos /images ou encore des
        gestes. Se tenir trop près, toucher ou bien se frotter à quelqu’un, est par
        exemple susceptible d’être considèré comme une agression ou du harcèlement
        sexuel.<br>
        § racistes, discriminants, ou stigmatisants en raison notamment de : l’origine,
        la croyance, la couleur de peau, la religion réelle ou supposée (par exemple :
        chrétienne, musulmane, juive), l’habillement, le genre, l’orientation sexuelle,
        l’apparence physique ou autre.
        § homophobes ou sexistes,
        § discriminants ou stigmatisants en raison du physique, du handicap,
        § incitant à la haine, la violence, le terrorisme ou la barbarie,
        § malveillants ou intimes et personnels ; et néanmoins divulgués sans le
        consentement de la personne concernée.
        <br>
        Le festival ne tolère aucun comportement pouvant porter atteinte à ses participants.
        Si je suis aperçu(e) ou signalé(e) comme ayant harcelé quelqu’un, des mesures
        seront prises.<br>
        Risques encourus : Le harcèlement est passible de 1 an de prison et 15 000 €
        d’amende Le harcèlement sexuel est passible de 2 ans de prison et de 30 000 €
        d’amende.<br>
        Mesure appliquée : Tout acte ou propos malveillant ou relevant du harcèlement
        entraînera l’exclusion de son auteur<br><br>
        <h2>4. la vie privée de chacun</h2><br>
        Il est impératif que chacun puisse bénéficier du confort et de l’intimité́ dont il souhaite.
        Ainsi, je respecte la vie privée de chacun. Ceci peut notamment se traduire par le fait
        de demander à un cosplayeur ou un influenceur s’il est possible de le prendre en photo
        et de ne pas faire de photo sans son consentement. Cela peut également se traduire
        par l’acceptation d’un éventuel refus de la part d’un cosplayeur ou d’un influenceur
        d’être pris en photo.<br><br>
    <h2>5. le matériel et la propriété des tiers</h2><br>
        Le vol n’est pas toléré́, aussi bien dans la loi qu’au festival. Ainsi, si je fais l’objet d’un
        constat de vol d’un objet par un autre visiteur ou exposant ou si je pénètre sur un stand
        sans y être autorisé, des mesures seront prises.<br>
        Aucun bien d’un participant au festival ne doit être abimé ou détérioré́. Ainsi, je fais
        attention aux produits mis à ma disposition par les exposants ou d’autres participants.
        Risques encourus : Le vol est passible de 3 ans de prison et de 45 000 € d’amende.
        Mesure appliquée au festival : Tout comportement contrevenant entraînera l’exclusion
        de son auteur.<br>

